package com.witribe.action;

import com.witribe.actionform.LoginForm;
import com.witribe.constants.WitribeConstants;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;

/**
 *
 * @author SC43278
 */
public class ForgotPasswordAction extends BaseAction {
    
    /** Creates a new instance of ForgotPasswordAction */
    public ForgotPasswordAction() {
    }
     public ActionForward execute(ActionMapping mapping,ActionForm form,HttpServletRequest req,HttpServletResponse res) throws Exception{
    
         LoginForm lform = (LoginForm)form;
         req.setAttribute("breadcrumb",WitribeConstants.BREADCRUMB);
         req.setAttribute(WitribeConstants.HEADING,WitribeConstants.WEB_PORTAL_FORGOTPASSWORD);
         return mapping.findForward("forgotpwd");
}
}