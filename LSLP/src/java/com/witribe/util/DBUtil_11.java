package com.witribe.util;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

/**
 *
 * @author SC43278
 */
public class DBUtil_11 {
    
    
    final static String urlThin="jdbc:oracle:thin:@172.16.152.92:1521:PINDB";
    final static String username="webapp";
    final static String password="webapp";
    
    public static Connection getConnection() throws SQLException {

        Connection con = null;
        DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
        con = DriverManager.getConnection(urlThin, username, password);
        return con;        
}
    public static void closeConnection(Connection con) throws SQLException {

         if (con != null) {
                try {
                     con.close();
                } catch (SQLException e) {
                    throw e;
                }
            }   
         
}
    
        public static void closeStatement(Statement stmt) throws SQLException {

         if (stmt != null) {
                try {
                     stmt.close();
                } catch (SQLException e) {
                    throw e;
                }
            }   
         
}
        public static void closePreparedStatement(PreparedStatement pstmt) throws SQLException {

         if (pstmt != null) {
                try {
                     pstmt.close();
                } catch (SQLException e) {
                    throw e;
                }
            }   
         
}
public static void closeResultSet(ResultSet rs) throws SQLException {

         if (rs != null) {
                try {
                     rs.close();
                } catch (SQLException e) {
                    throw e;
                }
            }   
         
}
    public static void main(String args[]){
        Connection con = null;
        try{
             con = getConnection();
        } catch(SQLException se){
            se.printStackTrace();
        }
        finally {

            if (con != null) {
                try {
                     con.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
                   
        }
        }
   
   }
  

